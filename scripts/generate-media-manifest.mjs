import { readdir, mkdir, writeFile } from 'node:fs/promises'
import { extname, join, relative, sep } from 'node:path'

const root = process.cwd()
const publicRoot = join(root, 'public', 'projects')
const sourceRoot = join(root, 'Projects')
const outputFile = join(root, 'src', 'data', 'mediaManifest.generated.ts')
const imageExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.svg'])
const videoExtensions = new Set(['.mp4', '.webm', '.mov'])

async function walk(directory) {
  const entries = await readdir(directory, { withFileTypes: true })
  const files = []
  for (const entry of entries) {
    const path = join(directory, entry.name)
    if (entry.isDirectory()) files.push(...await walk(path))
    else files.push(path)
  }
  return files
}

const toUrl = (path) => encodeURI('/projects/' + relative(publicRoot, path).split(sep).join('/'))

const toLabel = (path) => relative(publicRoot, path)
  .split(sep)
  .at(-1)
  .replace(extname(path), '')
  .replaceAll('_', ' ')
  .replace(/\s+/g, ' ')
  .trim()

const deployedFiles = (await walk(publicRoot))
  .filter((path) => imageExtensions.has(extname(path).toLowerCase()) || videoExtensions.has(extname(path).toLowerCase()))
  .sort((a, b) => a.localeCompare(b))

const groups = new Map()
for (const path of deployedFiles) {
  const relativePath = relative(publicRoot, path)
  const folder = relativePath.split(sep)[0]
  if (!groups.has(folder)) groups.set(folder, { folder, images: [], videos: [] })
  const media = { src: toUrl(path), label: toLabel(path), filename: relativePath.split(sep).join('/') }
  const type = videoExtensions.has(extname(path).toLowerCase()) ? 'videos' : 'images'
  groups.get(folder)[type].push(media)
}

let sourceOriginals = []
try {
  sourceOriginals = (await walk(sourceRoot)).filter((path) => videoExtensions.has(extname(path).toLowerCase()))
} catch {
  sourceOriginals = []
}

for (const source of sourceOriginals) {
  const relativePath = relative(sourceRoot, source)
  const deployedRelative = relativePath.replace(/\.mov$/i, '.mp4')
  const deployedPath = join(publicRoot, deployedRelative)
  if (!deployedFiles.includes(deployedPath)) {
    throw new Error(`Missing deployed video counterpart for source media: ${relativePath}`)
  }
}

const manifest = [...groups.values()]
const imageCount = manifest.reduce((count, group) => count + group.images.length, 0)
const videoCount = manifest.reduce((count, group) => count + group.videos.length, 0)
const contents = `// AUTO-GENERATED by scripts/generate-media-manifest.mjs\n// Add media under public/projects and run npm run media:manifest.\n\nexport type MediaItem = { src: string; label: string; filename: string }\nexport type MediaGroup = { folder: string; images: MediaItem[]; videos: MediaItem[] }\n\nexport const mediaManifest: MediaGroup[] = ${JSON.stringify(manifest, null, 2)}\n\nexport const mediaAudit = ${JSON.stringify({ projects: manifest.length, images: imageCount, videos: videoCount, sourceOriginals: sourceOriginals.length }, null, 2)} as const\n`

await mkdir(join(root, 'src', 'data'), { recursive: true })
await writeFile(outputFile, contents)
console.log(`Media manifest: ${manifest.length} projects, ${imageCount} images, ${videoCount} videos, ${sourceOriginals.length} preserved originals`)
